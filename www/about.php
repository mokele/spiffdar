<h2>What is Spiffdar?</h2>
<p>
    Spiffdar is aiming to be a service you can use to organise and share playlists while integrating tightly with <a href="http://www.playdar.org/">Playdar</a> to provide playability.
</p>
<p>
    Much like <a href="http://www.playdar.org/">Playdar</a>, Spiffdar is a very young project that does not yet do all the things you might expect it to do, but is still usable.  Keep an eye on the <a href="/changelog.php">change log</a> for future updates to the live copy of Spiffdar.
</p>
<h2>How to contribute</h2>
<p>
    The code for Spiffdar is available on <a href="http://github.com/">github</a> at <a href="http://github.com/mokele/spiffdar/tree/master">http://github.com/mokele/spiffdar/tree/master</a>, so don't hesitate to clone/fork and throw pull requests to github account 'mokele'.  You can find us in the #playdar IRC channel on freenode along with the rest of the <a href="http://www.playdar.org/">Playdar</a> community.
</p>

